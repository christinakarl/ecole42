#include "ft_printf.h"
#include <stdio.h>

void	ft_put_di(int *put, int n)
{
	printf("%d\n", n);
	if (n == -2147483648)
	{
		put += write(1, "-", 1);
		put += write(1, "2", 1);
		n = 147483648;
	}
	else if (n < 0)
	{
		put += write(1, "-", 1);
		n = n * (-1);
	}
	while (n >= 10)
	{
		ft_put_c(put, (n % 10 + 48));
		n = n / 10;
	}
	if (n < 10)
		ft_put_c(put, (n + 48));
}
