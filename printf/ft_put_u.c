#include "ft_printf.h"

void	ft_put_u(int *put, unsigned int n)
{
	while (n >= 10)
	{
		ft_put_c(put, (n % 10 + 48));
		n = n / 10;
	}
	if (n < 10)
		ft_put_c(put, (n + 48));
}
